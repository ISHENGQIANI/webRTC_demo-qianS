package cn.teclub.ha3.utils;

import org.slf4j.LoggerFactory;
import org.slf4j.Logger;

import cn.teclub.common.ChuyuUtil;

@SuppressWarnings("unused")
public class StObject extends StFamily
{
    ////////////////////////////////////////////////////////////////////////////
    // STATIC MEMBER
    ////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////
    // Inner Class
    ////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////
    // Properties
    ////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////
    // Methods
    ////////////////////////////////////////////////////////////////////////////


    protected static ChuyuUtil  util = ChuyuUtil.getInstance();
    protected Logger            log  = LoggerFactory.getLogger(this.getClass());


    ////  very commonly used utilities ////
    protected static void assertTrue(final boolean condition, final String fail_msg){
        util.assertTrue(condition, fail_msg);
    }

    protected static void assertTrue(final boolean condition){
        util.assertTrue(condition);
    }
}
